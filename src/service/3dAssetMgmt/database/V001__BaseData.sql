CREATE TABLE IF NOT EXISTS `msg_md_asset_AssetSummary` (
    `AssetSummaryID` BIGINT(20) NOT NULL AUTO_INCREMENT COMMENT 'Primary key for AssetSummary',
    `AssetUID` BINARY(16) NOT NULL COMMENT 'Unique identifier for asset',
    `AssetName` VARCHAR(128) COLLATE UTF8MB4_UNICODE_CI NULL DEFAULT NULL,
    `SerialNumber` VARCHAR(128) COLLATE UTF8MB4_UNICODE_CI NOT NULL,
    `MakeCode` VARCHAR(128) COLLATE UTF8MB4_UNICODE_CI NOT NULL,
    `Model` VARCHAR(128) COLLATE UTF8MB4_UNICODE_CI NULL DEFAULT NULL,
    `Family` VARCHAR(128) COLLATE UTF8MB4_UNICODE_CI NULL DEFAULT NULL,
    `ModelYear` SMALLINT(6) NULL DEFAULT NULL,
    `IconKey` INT(20) NULL DEFAULT NULL,
	`LegacyAssetID` BIGINT(20) NULL DEFAULT NULL,
	`EquipmentVIN` VARCHAR(70) NULL DEFAULT NULL,
	`OwningCustomerUID` BINARY(16) NULL DEFAULT NULL,
    `ActionUTC` DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
    `ReceivedUTC` DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
    `RowUpdatedUTC` DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
    PRIMARY KEY (`AssetSummaryID`),
    UNIQUE INDEX `AssetUID_UNIQUE` (`AssetUID` ASC),
    INDEX `idx_SerialNumber` (`SerialNumber` ASC),
    INDEX `idx_MakeCode` (`MakeCode` ASC),
    INDEX `idx_Model` (`Model` ASC),
    INDEX `idx_Family` (`Family` ASC),
    INDEX `idx_AssetName` (`AssetName` ASC)
)  ENGINE=INNODB AUTO_INCREMENT=0 COLLATE = UTF8MB4_UNICODE_CI;
